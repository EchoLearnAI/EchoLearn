// @host localhost:8080
// @BasePath /api/v1
func main() {
	r := gin.Default()

	// Enable CORS
	r.Use(CORSMiddleware())

	// Routes
	v1 := r.Group("/api/v1")
	{
		v1.GET("/ping", Ping)
		v1.GET("/difficulties", GetDifficulties)
		v1.GET("/topics", GetTopics)
		v1.GET("/questions/:difficulty/:topic", GetQuestionsByDifficultyAndTopic)
		v1.POST("/submit", SubmitAnswer) // Assuming we might want to submit answers one by one or all at once
	}

	// Swagger
	r.GET("/swagger/*any", ginSwagger.WrapHandler(swaggerFiles.Handler))

	r.Run(":8080") // listen and serve on 0.0.0.0:8080
}

// Ping godoc
// @Summary Ping the server
// @Description get "pong" response to check if server is alive
// @Tags health
// @Accept  json
// @Produce  json
// @Success 200 {object} map[string]string
// @Router /ping [get]
func Ping(c *gin.Context) {
	c.JSON(http.StatusOK, gin.H{
		"message": "pong",
	})
}

// CORSMiddleware creates a new middleware handler for CORS
func CORSMiddleware() gin.HandlerFunc {
	return func(c *gin.Context) {
		c.Writer.Header().Set("Access-Control-Allow-Origin", "*") // Allow all origins
		c.Writer.Header().Set("Access-Control-Allow-Credentials", "true")
		c.Writer.Header().Set("Access-Control-Allow-Headers", "Content-Type, Content-Length, Accept-Encoding, X-CSRF-Token, Authorization, accept, origin, Cache-Control, X-Requested-With")
		c.Writer.Header().Set("Access-Control-Allow-Methods", "POST, OPTIONS, GET, PUT, DELETE")

		if c.Request.Method == "OPTIONS" {
			c.AbortWithStatus(204)
			return
		}

		c.Next()
	}
}

